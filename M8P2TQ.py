# This program creates a list that counts up to 10.
numList = []
counter = 0
# The use of while gives the counter a number to count to and that is less than 10.
while counter < 11:
    numList.append(counter)
    counter += 1
# Counter =+ 1 adds 1 everytime the loop goes around and when it hits 10 it stops.
print(numList)